const generatedHeader = "// Code generated by idiomgen. DO NOT EDIT."

// Generate runs the full idiomgen pipeline: merge spec+overlay, render templates, write files.
// If capiDir is non-empty and the merged spec has bridge-worthy features, bridge files
// are generated into capiDir.
func Generate(
	spec specmodel.Spec,
	overlay overlaymodel.Overlay,
	tmplDir string,
	outDir string,
	capiDir ...string,
) error {
	merged := Merge(spec, overlay)

	if err := os.MkdirAll(outDir, 0o755); err != nil {
		return fmt.Errorf("mkdir: %w", err)
	}

	// Track all files we intend to write, for stale file cleanup.
	written := make(map[string]bool)

	// Partition free functions: factory functions (returning an opaque type) go
	// into that type's per-type file; the rest stay in functions.go.
	typeFreeFuncs := make(map[string][]MergedFreeFunction) // key: Go type name
	var sharedFreeFuncs []MergedFreeFunction
	for _, f := range merged.FreeFunctions {
		if f.IsHandleReturn {
			targetType := strings.TrimPrefix(f.Returns, "*")
			if _, ok := merged.OpaqueTypes[targetType]; ok {
				typeFreeFuncs[targetType] = append(typeFreeFuncs[targetType], f)
				continue
			}
		}
		sharedFreeFuncs = append(sharedFreeFuncs, f)
	}
	merged.FreeFunctions = sharedFreeFuncs

	// Shared templates rendered against the full MergedSpec.
	sharedTemplates := map[string]string{
		"package.go":   "package.go.tmpl",
		"errors.go":    "errors.go.tmpl",
		"functions.go": "functions.go.tmpl",
	}
	if merged.Lifecycle != nil {
		sharedTemplates["lifecycle.go"] = "lifecycle.go.tmpl"
	}

	for outFile, tmplFile := range sharedTemplates {
		tmplPath := filepath.Join(tmplDir, tmplFile)
		if _, err := os.Stat(tmplPath); os.IsNotExist(err) {
			continue
		}

		content, err := RenderTemplateFile(tmplPath, merged)
		if err != nil {
			return fmt.Errorf("render %s: %w", tmplFile, err)
		}

		outPath := filepath.Join(outDir, outFile)
		if err := os.WriteFile(outPath, []byte(content), 0o644); err != nil {
			return fmt.Errorf("write %s: %w", outFile, err)
		}
		written[outFile] = true
	}

	// Per-value-enum files: one file per value enum type.
	enumTmplPath := filepath.Join(tmplDir, "value_enum_file.go.tmpl")
	if _, err := os.Stat(enumTmplPath); err == nil {
		for _, ve := range merged.ValueEnums {
			data := PerValueEnumData{
				PackageName: merged.PackageName,
				Enum:        ve,
			}
			content, err := RenderAny(enumTmplPath, data)
			if err != nil {
				return fmt.Errorf("render enum %s: %w", ve.GoName, err)
			}
			fileName := toSnakeCase(ve.GoName) + ".go"
			outPath := filepath.Join(outDir, fileName)
			if err := os.WriteFile(outPath, []byte(content), 0o644); err != nil {
				return fmt.Errorf("write %s: %w", fileName, err)
			}
			written[fileName] = true
		}
	}

	// Per-type-alias files: one file per type alias.
	aliasTmplPath := filepath.Join(tmplDir, "type_alias_file.go.tmpl")
	if _, err := os.Stat(aliasTmplPath); err == nil {
		allAliases := make([]MergedTypeAlias, 0, len(merged.TypeAliases)+len(merged.CallbackStructAliases))
		allAliases = append(allAliases, merged.TypeAliases...)
		allAliases = append(allAliases, merged.CallbackStructAliases...)
		for _, alias := range allAliases {
			data := PerTypeAliasData{
				PackageName:   merged.PackageName,
				SourcePackage: merged.SourcePackage,
				Alias:         alias,
			}
			content, err := RenderAny(aliasTmplPath, data)
			if err != nil {
				return fmt.Errorf("render alias %s: %w", alias.GoName, err)
			}
			fileName := toSnakeCase(alias.GoName) + ".go"
			outPath := filepath.Join(outDir, fileName)
			if err := os.WriteFile(outPath, []byte(content), 0o644); err != nil {
				return fmt.Errorf("write %s: %w", fileName, err)
			}
			written[fileName] = true
		}
	}

	// Per-callback files: one file per callback type with a Go type declaration.
	cbTmplPath := filepath.Join(tmplDir, "callback_file.go.tmpl")
	if _, err := os.Stat(cbTmplPath); err == nil {
		for _, cb := range merged.Callbacks {
			if cb.GoCallbackType == "" {
				continue
			}
			data := PerCallbackData{
				PackageName: merged.PackageName,
				Callback:    cb,
			}
			content, err := RenderAny(cbTmplPath, data)
			if err != nil {
				return fmt.Errorf("render callback %s: %w", cb.GoCallbackType, err)
			}
			fileName := toSnakeCase(cb.GoCallbackType) + ".go"
			outPath := filepath.Join(outDir, fileName)
			if err := os.WriteFile(outPath, []byte(content), 0o644); err != nil {
				return fmt.Errorf("write %s: %w", fileName, err)
			}
			written[fileName] = true
		}
	}

	// Per-type files: one file per opaque type containing struct + constructor + destructor + methods + factory functions.
	typeTmplPath := filepath.Join(tmplDir, "type_file.go.tmpl")
	if _, err := os.Stat(typeTmplPath); err == nil {
		// Sort type names for deterministic output.
		typeNames := make([]string, 0, len(merged.OpaqueTypes))
		for name := range merged.OpaqueTypes {
			typeNames = append(typeNames, name)
		}
		sort.Strings(typeNames)

		for _, typeName := range typeNames {
			opaqueType := merged.OpaqueTypes[typeName]

			// Collect methods belonging to this type.
			var methods []MergedMethod
			for _, m := range merged.Methods {
				if m.ReceiverType == typeName {
					methods = append(methods, m)
				}
			}

			data := PerTypeData{
				PackageName:   merged.PackageName,
				SourcePackage: merged.SourcePackage,
				Type:          opaqueType,
				Methods:       methods,
				FreeFunctions: typeFreeFuncs[typeName],
				OpaqueTypes:   merged.OpaqueTypes,
			}

			content, err := RenderPerType(typeTmplPath, data)
			if err != nil {
				return fmt.Errorf("render type %s: %w", typeName, err)
			}

			fileName := toSnakeCase(typeName) + ".go"
			outPath := filepath.Join(outDir, fileName)
			if err := os.WriteFile(outPath, []byte(content), 0o644); err != nil {
				return fmt.Errorf("write %s: %w", fileName, err)
			}
			written[fileName] = true
		}
	}

	// Clean up stale generated files.
	if err := cleanStaleFiles(outDir, written); err != nil {
		return fmt.Errorf("cleanup: %w", err)
	}

	// Generate bridge files into capi directory if requested.
	resolvedCapiDir := ""
	if len(capiDir) > 0 {
		resolvedCapiDir = capiDir[0]
	}
	hasBridgeFeatures := len(merged.CallbackStructs) > 0 || len(merged.StructAccessors) > 0 || merged.Lifecycle != nil || merged.ExtraBridgeC != "" || merged.ExtraBridgeGo != ""
	if resolvedCapiDir != "" && hasBridgeFeatures {
		if err := generateBridgeFiles(merged, tmplDir, resolvedCapiDir); err != nil {
			return fmt.Errorf("bridge: %w", err)
		}
	}

	// Format all generated Go files.
	if err := formatGoFilesInDir(outDir); err != nil {
		return fmt.Errorf("format: %w", err)
	}

	return nil
}

// cleanStaleFiles removes generated .go files in outDir that are not in the written set.
func cleanStaleFiles(outDir string, written map[string]bool) error {
	entries, err := os.ReadDir(outDir)
	if err != nil {
		return err
	}
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".go") {
			continue
		}
		if written[e.Name()] {
			continue
		}
		// Only remove files that were generated by idiomgen.
		path := filepath.Join(outDir, e.Name())
		content, err := os.ReadFile(path)
		if err != nil {
			continue
		}
		if strings.HasPrefix(string(content), generatedHeader) {
			if err := os.Remove(path); err != nil {
				return fmt.Errorf("remove stale %s: %w", e.Name(), err)
			}
		}
	}
	return nil
}

// LoadSpec reads and parses a spec YAML file.
func LoadSpec(path string) (specmodel.Spec, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return specmodel.Spec{}, err
	}
	var spec specmodel.Spec
	if err := yaml.Unmarshal(data, &spec); err != nil {
		return specmodel.Spec{}, err
	}
	return spec, nil
}

// LoadOverlay reads and parses an overlay YAML file.
func LoadOverlay(path string) (overlaymodel.Overlay, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return overlaymodel.Overlay{}, err
	}
	var ov overlaymodel.Overlay
	if err := yaml.Unmarshal(data, &ov); err != nil {
		return overlaymodel.Overlay{}, err
	}
	return ov, nil
}

// LoadOverlayOrEmpty loads an overlay from path, or returns a zero-value Overlay if path is empty.
func LoadOverlayOrEmpty(path string) (overlaymodel.Overlay, error) {
	if path == "" {
		return overlaymodel.Overlay{}, nil
	}
	return LoadOverlay(path)
}

// generateBridgeFiles renders bridge templates into the capi directory.
func generateBridgeFiles(
	merged MergedSpec,
	tmplDir string,
	capiDir string,
) error {
	if err := os.MkdirAll(capiDir, 0o755); err != nil {
		return fmt.Errorf("mkdir capi: %w", err)
	}

	// Extract C #include lines from existing .go files in capiDir.
	cIncludes, err := extractCIncludes(capiDir)
	if err != nil {
		return fmt.Errorf("extract includes: %w", err)
	}

	// Determine the capi package name from the directory name.
	pkgName := filepath.Base(capiDir)

	data := BridgeData{
		PackageName:     pkgName,
		CIncludes:       cIncludes,
		CallbackStructs: merged.CallbackStructs,
		StructAccessors: merged.StructAccessors,
		Lifecycle:       merged.Lifecycle,
		ExtraBridgeC:    merged.ExtraBridgeC,
		ExtraBridgeGo:   merged.ExtraBridgeGo,
	}

	bridgeWritten := make(map[string]bool)

	// Render callback bridge files if there are callback structs or struct accessors.
	if len(merged.CallbackStructs) > 0 || len(merged.StructAccessors) > 0 {
		bridgeTemplates := map[string]string{
			"bridge_registry.go": "bridge_registry.go.tmpl",
			"bridge_c.go":        "bridge_c.go.tmpl",
			"bridge_export.go":   "bridge_export.go.tmpl",
		}
		for outFile, tmplFile := range bridgeTemplates {
			tmplPath := filepath.Join(tmplDir, tmplFile)
			if _, statErr := os.Stat(tmplPath); os.IsNotExist(statErr) {
				continue
			}

			content, renderErr := RenderBridgeTemplate(tmplPath, data)
			if renderErr != nil {
				return fmt.Errorf("render %s: %w", tmplFile, renderErr)
			}

			outPath := filepath.Join(capiDir, outFile)
			if writeErr := os.WriteFile(outPath, []byte(content), 0o644); writeErr != nil {
				return fmt.Errorf("write %s: %w", outFile, writeErr)
			}
			bridgeWritten[outFile] = true
		}
	}

	// Render lifecycle bridge files if lifecycle is configured.
	if merged.Lifecycle != nil {
		lifecycleTemplates := map[string]string{
			"bridge_lifecycle.go":        "bridge_lifecycle.go.tmpl",
			"bridge_lifecycle_export.go": "bridge_lifecycle_export.go.tmpl",
		}
		for outFile, tmplFile := range lifecycleTemplates {
			tmplPath := filepath.Join(tmplDir, tmplFile)
			if _, statErr := os.Stat(tmplPath); os.IsNotExist(statErr) {
				continue
			}

			content, renderErr := RenderBridgeTemplate(tmplPath, data)
			if renderErr != nil {
				return fmt.Errorf("render %s: %w", tmplFile, renderErr)
			}

			outPath := filepath.Join(capiDir, outFile)
			if writeErr := os.WriteFile(outPath, []byte(content), 0o644); writeErr != nil {
				return fmt.Errorf("write %s: %w", outFile, writeErr)
			}
			bridgeWritten[outFile] = true
		}
	}

	// Clean stale bridge_*.go files.
	if cleanErr := cleanStaleBridgeFiles(capiDir, bridgeWritten); cleanErr != nil {
		return fmt.Errorf("cleanup bridge: %w", cleanErr)
	}

	// Format generated bridge files.
	for name := range bridgeWritten {
		if err := formatGoFile(filepath.Join(capiDir, name)); err != nil {
			return fmt.Errorf("format bridge: %w", err)
		}
	}

	return nil
}

// extractCIncludes reads all .go files in dir and extracts #include lines from CGo preambles.
func extractCIncludes(dir string) ([]string, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		if os.IsNotExist(err) {
			return nil, nil
		}
		return nil, err
	}

	seen := make(map[string]bool)
	var includes []string

	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".go") {
			continue
		}
		// Skip bridge files to avoid reading our own generated includes.
		if strings.HasPrefix(e.Name(), "bridge_") {
			continue
		}

		path := filepath.Join(dir, e.Name())
		fileIncludes, readErr := extractIncludesFromFile(path)
		if readErr != nil {
			continue
		}
		for _, inc := range fileIncludes {
			if !seen[inc] {
				seen[inc] = true
				includes = append(includes, inc)
			}
		}
	}

	return includes, nil
}

// extractIncludesFromFile reads a Go source file and extracts #include lines
// from the CGo preamble (the comment block immediately preceding `import "C"`).
func extractIncludesFromFile(path string) ([]string, error) {
	f, err := os.Open(path)
	if err != nil {
		return nil, err
	}
	defer f.Close()

	var includes []string
	inComment := false
	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := scanner.Text()
		trimmed := strings.TrimSpace(line)

		if trimmed == "/*" {
			inComment = true
			continue
		}
		if trimmed == "*/" {
			inComment = false
			continue
		}
		if inComment && strings.HasPrefix(trimmed, "#include") {
			includes = append(includes, trimmed)
		}
	}

	return includes, scanner.Err()
}

// formatGoFile formats a single Go source file in place using go/format.
func formatGoFile(path string) error {
	src, err := os.ReadFile(path)
	if err != nil {
		return fmt.Errorf("reading %s: %w", filepath.Base(path), err)
	}
	formatted, err := format.Source(src)
	if err != nil {
		return fmt.Errorf("formatting %s: %w", filepath.Base(path), err)
	}
	return os.WriteFile(path, formatted, 0o644)
}

// formatGoFilesInDir formats all .go files in the given directory using go/format.
func formatGoFilesInDir(dir string) error {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return err
	}
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".go") {
			continue
		}
		if err := formatGoFile(filepath.Join(dir, e.Name())); err != nil {
			return err
		}
	}
	return nil
}

// cleanStaleBridgeFiles removes bridge_*.go files in capiDir that are not in the written set.
func cleanStaleBridgeFiles(capiDir string, written map[string]bool) error {
	entries, err := os.ReadDir(capiDir)
	if err != nil {
		return err
	}
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".go") {
			continue
		}
		if !strings.HasPrefix(e.Name(), "bridge_") {
			continue
		}
		if written[e.Name()] {
			continue
		}
		// Only remove files that were generated by idiomgen.
		path := filepath.Join(capiDir, e.Name())
		content, readErr := os.ReadFile(path)
		if readErr != nil {
			continue
		}
		if strings.HasPrefix(string(content), generatedHeader) {
			if removeErr := os.Remove(path); removeErr != nil {
				return fmt.Errorf("remove stale bridge %s: %w", e.Name(), removeErr)
			}
		}
	}
	return nil
}
