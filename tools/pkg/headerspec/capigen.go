// funcReturnGoType determines the Go return type for a function.
func funcReturnGoType(
	cRetType string,
	typedefByName map[string]*TypedefInfo,
) string {
	cRetType = stripNullabilityAnnotations(cRetType)

	if cRetType == "" || cRetType == "void" {
		return ""
	}

	// Pointer to opaque struct.
	if strings.HasSuffix(cRetType, " *") {
		base := strings.TrimSuffix(cRetType, " *")
		base = strings.TrimPrefix(base, "const ")
		base = strings.TrimPrefix(base, "struct ")
		base = strings.TrimSpace(base)

		if td, ok := typedefByName[base]; ok && td.IsOpaqueStruct {
			return "*" + base
		}
		return "*" + cTypeToGoType(base)
	}

	// Check for typedef types (enum typedefs get their own Go type).
	if td, ok := typedefByName[cRetType]; ok {
		if td.IsEnumTypedef {
			return cRetType
		}
	}

	return cTypeToGoType(cRetType)
}

// returnConversion generates code to convert a C return value to Go.
func returnConversion(
	cRetType string,
	goRetType string,
	typedefByName map[string]*TypedefInfo,
) string {
	cRetType = stripNullabilityAnnotations(cRetType)

	// Pointer return: reinterpret the C pointer via unsafe.Pointer.
	// The pattern *(**ALooper)(unsafe.Pointer(&__ret)) takes the address of
	// the *C.ALooper value, casts to **ALooper, and dereferences to get *ALooper.
	if strings.HasPrefix(goRetType, "*") {
		return fmt.Sprintf("\t__v := *(*%s)(unsafe.Pointer(&__ret))\n\treturn __v\n", goRetType)
	}

	// Bool return.
	if goRetType == "bool" {
		return "\t__v := (bool)(__ret)\n\treturn __v\n"
	}

	// Enum typedef or scalar return.
	return fmt.Sprintf("\t__v := (%s)(__ret)\n\treturn __v\n", goRetType)
}

// unexport returns the unexported version of a Go identifier.
func unexport(name string) string {
	if len(name) == 0 {
		return name
	}
	first := strings.ToLower(name[:1])
	return first + name[1:]
}



