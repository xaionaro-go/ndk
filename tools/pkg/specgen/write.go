package specgen

import (
	"fmt"
	"os"
	"path/filepath"

	"github.com/xaionaro-go/ndk/tools/pkg/specmodel"

	"gopkg.in/yaml.v3"
)

const generatedHeader = "# Code generated by specgen. DO NOT EDIT.\n\n"

// WriteSpec marshals spec to YAML and writes it to path, creating parent
// directories as needed. The file is prefixed with a generated-code header.
func WriteSpec(spec specmodel.Spec, path string) error {
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return fmt.Errorf("create directories: %w", err)
	}

	data, err := yaml.Marshal(spec)
	if err != nil {
		return fmt.Errorf("marshal spec: %w", err)
	}

	out := append([]byte(generatedHeader), data...)
	if err := os.WriteFile(path, out, 0o644); err != nil {
		return fmt.Errorf("write file: %w", err)
	}

	return nil
}
